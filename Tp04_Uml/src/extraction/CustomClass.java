package extraction;


public class CustomClass {
    private String name;
    private String type;
 
  
    
   

	public CustomClass(String name) {
        this.name = name;
      
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

  

}
